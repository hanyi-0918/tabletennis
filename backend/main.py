def search_player_fuzzy(query: str) -> tuple:
    """模糊搜索球员，返回 (匹配名, 对手列表, 是否模糊)"""
    q = query.strip()
    if not q:
        return None, None, False
    # 精确匹配
    for name in PLAYERS.keys():
        if name == q or q in name:
            return name, PLAYERS[name], False
    # 包含匹配
    for name in PLAYERS.keys():
        if q in name or name.startswith(q) or q.startswith(name):
            return name, PLAYERS[name], True
    return None, None, False


class OpponentsResponse(BaseModel):
    player_name: str
    opponents: List[str]
    is_fuzzy_match: bool


class PlayersResponse(BaseModel):
    players: List[str]


class H2HResponse(BaseModel):
    player_a: str
    player_b: str
    player_a_wins: int
    player_b_wins: int
    total_matches: int
    player_a_major_wins: int = 0
    player_b_major_wins: int = 0
    recent_matches: List[str]


